	public function catList()
	{
		$r[] = array( 'id' => 0, 'label' => __( 'All', 'mega' ));

		$this->buildSubjects( 'category', $r );

		return $r;
	}

	public function pageList()
	{
		$r[] = array( 'id' => 0, 'label' => __( 'All', 'mega' ));

		$this->buildSubjects( 'pages', $r );

		return $r;
	}

	public function postList()
	{
		$r[] = array( 'id' => 0, 'label' => __( 'All', 'mega' ));

		$this->buildSubjects( 'posts', $r );

		return $r;
	}

	public function buildSubjects( $type, &$r = array(), $id = 0 )
	{
		$args = array(
			'orderby'	=> 'count',
			'order'		=> 'DESC',
			'parent'	=> $id
		);

		switch( $type )
		{
			case 'category' :
				$subjects = get_categories( $args );
				break;

			case 'pages' :
				$subjects = get_pages( $args );
				break;

			case 'posts' :
				$subjects = get_posts( wp_parse_args( array( 'posts_per_page' => -1, 'post_parent' => $id ), $args ));
				break;
		}

		if ( $subjects && count( $subjects ) > 0 )
		{
			foreach( $subjects as $subject )
			{
				switch( $type )
				{
					case 'category' :
						$subject_id = $subject->term_id;
						$label		= $subject->name . ' (' . $subject->category_count . ')';
					break;

					case 'pages' :
						$subject_id = $subject->ID;
						$label		= $subject->post_title;
					break;

					case 'posts' :
						$subject_id = $subject->ID;
						$label		= $subject->post_title;
					break;
				}

				$r[] = array( 'id' => $subject_id, 'label' => $label, 'children' => false );

				end($r);

				$this->buildSubjects( $type, $r[key($r)]['children'], $subject_id );
			}
		}
	}

