	public function templates2()
	{
		global $mega_package;

		$r = $this->templates();

		if ( isset( $r['front_page'] ) && $mega_package == 0 )
			unset( $r['front_page'] );

		if ( isset( $r['home'] ) && $mega_package == 0 )
			unset( $r['home'] );

		return $r;
	}

